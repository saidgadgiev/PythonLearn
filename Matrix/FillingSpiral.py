# ljust(). –ú–æ–∂–Ω–æ –æ–±–æ–π—Ç–∏—Å—å –∏ –±–µ–∑ ljust(), —Å–∏—Å—Ç–µ–º–∞ –ø—Ä–∏–º–µ—Ç –∏ —Ç–∞–∫–æ–µ —Ä–µ—à–µ–Ω–∏–µ 

# Sample Input 1:
# 4 5
# Sample Output 1:
# 1  2  3  4  5
# 14 15 16 17 6
# 13 20 19 18 7
# 12 11 10 9  8

s = "6 8".split()
n,m = int(s[0]), int(s[1])


matr = [[0] * m for _ in range(n)] 



w = 1
for k in range(min(n // 2 + 1, m //2 + 1)):  # –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –æ–±–æ—Ä–æ—Ç–æ–≤
    for j in range(k, m - k):  # –∑–∞–ø–æ–ª–Ω–µ–Ω–∏–µ –≤–ø—Ä–∞–≤–æ
        if matr[k][j] == 0:  # —É—Å–ª–æ–≤–∏–µ –∏–∑–±–∞–≤–ª—è–µ—Ç –æ—Ç –ø–µ—Ä–µ–∑–∞–ø–∏—Å—ã–≤–∞–Ω–∏—è –∑–Ω–∞—á–µ–Ω–∏–π
            matr[k][j] = w 
            w += 1
    for i in range(1 + k, n - k):  # –∑–∞–ø–æ–ª–Ω–µ–Ω–∏–µ –≤–Ω–∏–∑
        if matr[i][m - k - 1] == 0:
            matr[i][m - k - 1] = w 
            w += 1
    for j in range(m - k - 2, k - 1, -1):  # –∑–∞–ø–æ–ª–Ω–µ–Ω–∏–µ –≤–ª–µ–≤–æ
        if matr[n - k - 1][j] == 0:
            matr[n - k - 1][j] = w 
            w += 1
    for i in range(n - k - 2, k, -1):  # –∑–∞–ø–æ–ª–Ω–µ–Ω–∏–µ –≤–≤–µ—Ä—Ö
        if matr[i][k] == 0:
            matr[i][k] = w 
            w += 1
for i in range(n):  # –≤—ã–≤–æ–¥ –º–∞—Ç—Ä–∏—Ü—ã
    for j in range(m):
        print(str(matr[i][j]).ljust(3), end=' ')
    print()